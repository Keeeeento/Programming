package kadai12;

public class E {

	public static void main(String[] args) {
		double e = Math.E;
		double ee = Math.pow(e, 2.5);
		System.out.println(Math.pow(ee, 0.5));

		System.out.println(Math.pow(Math.E, 1.25));

		double a = Math.log(5) - Math.cos((3 * Math.PI) / 4);
		System.out.println(Math.log(5));
		System.out.println(Math.cos((3 * Math.PI) / 4));
		System.out.println(a);

	}

}
