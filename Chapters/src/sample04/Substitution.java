package sample04;

public class Substitution {

	public static void main(String[] args) {
		int a = 0;

		a += 5;
		System.out.println("a+=5の結果は" + a);

		a -= 2;
		System.out.println("a-=2の結果は" + a);

		a*=9;
		System.out.println("a*=9の結果は"+a);

		a/=2;
		System.out.println("a/=2の結果は"+a);

		a%=5;
		System.out.println("a%=5の結果は"+a);
	}

}
