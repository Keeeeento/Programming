package sample04;

public class Connection {

	public static void main(String[] args) {
		String s = "わたしは" + "太郎です"; //文字列を＋で連結
		System.out.println(s);

		String name = "太郎";
		String t = "わたしは" + name + "です"; // 文字列と変数を＋で連結
		System.out.println(t);
		
		System.out.println("わたしは"+name+"です");

	}

}
